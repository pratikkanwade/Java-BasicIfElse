//write a java program that checks a number from 0 to 5 and prints its spelling,if the number is greater than 5 print the numbre is greater than 5 

class Program{
	public static void main(String[]args){
		int x=6;
		
		if(x==0){
			System.out.println("ZERO");
		}
		else if(x==1){
			System.out.println("ONE");
		}
		else if(x==2){
			System.out.println("TWO");
		}
		else if(x==3){
			System.out.println("THREE");
		}
		else if(x==4){
			System.out.println("FOUR");
		}
		else if(x==5){
			System.out.println("FIVE");
		}
		else
			System.out.println("THE NUMBER IS GREATER THAN 5");
		
	}
}
