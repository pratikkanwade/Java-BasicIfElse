// write a java program, take a number, and print whether it is positive or negative

class Program{
	public static void main(String[] args){
		int x=0;

		if(x>0){
			System.out.println(x+"is Positive number");
		}
		else if(x<0){
			System.out.println(x+"is Negative number");
		}
		else
			System.out.println(x+"It is Zero number");


	}
}
